def generate_phase4d(var_defs: dict, corner_map: dict, components_page: str) -> str:
    # Build corner-only varDefs
    corner_var_defs = {k: v for k, v in var_defs.items() if k.startswith("corner")}
    if not corner_var_defs:
        corner_var_defs = var_defs

    # Build getCornerVar() body from corner_map
    corner_checks = []
    for val, key in sorted(corner_map.items(), key=lambda x: float(x[0]) if isinstance(x[0], (int, float)) else 999):
        corner_checks.append(f"  if (Math.abs(val - {val}) < 0.5) return '{key}';")
    # Always include the >10000 full-radius guard
    if not any("10000" in c for c in corner_checks):
        corner_checks.append("  if (val > 10000) return 'cornerFull';")
    corner_checks_str = "\n".join(corner_checks) if corner_checks else "  return null;"

    return f"""// Phase 4d: Corner radii — generated by figma_primitives prep-bind
{_page_boilerplate(components_page)}

{_render_js_obj('varDefs', corner_var_defs)}
{_load_vars_block()}

function getCornerVar(val) {{
{corner_checks_str}
  return null;
}}

const bound = [], errors = [];
const candidates = compPage.findAll(n => 'cornerRadius' in n && !n.id.includes(';'));

for (const node of candidates) {{
  if (node.cornerRadius === figma.mixed) continue;
  if (typeof node.cornerRadius !== 'number' || node.cornerRadius <= 0) continue;
  if (node.boundVariables?.cornerRadius) continue;
  const varKey = getCornerVar(node.cornerRadius);
  if (!varKey || !V[varKey]) continue;
  try {{
    node.setBoundVariable('cornerRadius', V[varKey]);
    bound.push({{ varKey, value: node.cornerRadius }});
  }} catch (e) {{ errors.push({{ nodeId: node.id, err: e.message.slice(0,80) }}); }}
}}

const tally = {{}};
for (const b of bound) tally[b.varKey] = (tally[b.varKey]||0)+1;
return {{ boundTotal: bound.length, tally, errorCount: errors.length, errors: errors.slice(0,5) }};
"""


def generate_summary(var_defs: dict, color_map: dict, corner_map: dict,
                     results: dict, failed: list, components_page: str,
                     phase1_path: str, phase2_path: str, out_dir: str) -> str:
    lines = [
        "# figma-bind-prep output",
        "",
        f"Generated from: {Path(phase1_path).name} + {Path(phase2_path).name}",
        f"COMPONENTS_PAGE: {components_page}",
        "",
        f"## Variable definitions ({len(var_defs)} resolved)",
    ]
    for k, v in var_defs.items():
        info = results.get(k, {})
        resolved = info.get("resolved", {})
        hex_str = resolved.get("hex", resolved.get("value", "?"))
        lines.append(f"  - {k}: {info.get('name', k)} = {hex_str}")

    if failed:
        lines += ["", f"## Failed ({len(failed)})", *[f"  - {f}" for f in failed]]

    lines += [
        "",
        "## Color map",
        *[f"  #{h} → {k}" for h, k in sorted(color_map.items())],
        "",
        "## Scripts generated",
        f"  - {out_dir}/phase4a-text-fills.js",
        f"  - {out_dir}/phase4b-surface-fills.js",
        f"  - {out_dir}/phase4c-strokes.js",
        f"  - {out_dir}/phase4d-corners.js",
    ]
    return "\n".join(lines) + "\n"


def run(phase1_path: str, phase2_path: str, config_path: str | None, out_dir: str):
    with open(phase1_path) as f:
        phase1 = json.load(f)
    with open(phase2_path) as f:
        phase2 = json.load(f)

    config = _load_config(config_path)
    components_page = config.get("componentsPage", "Components")

    var_defs, color_map, corner_map, warnings = _build_var_maps(phase1, phase2)

    for w in warnings:
        print(f"WARNING: {w}", file=sys.stderr)

    results = phase2.get("results", {})
    failed = phase2.get("failed", [])

    out = Path(out_dir)
    out.mkdir(parents=True, exist_ok=True)

    scripts = {
        "phase4a-text-fills.js": generate_phase4a(var_defs, color_map, components_page),
        "phase4b-surface-fills.js": generate_phase4b(var_defs, color_map, components_page),
        "phase4c-strokes.js": generate_phase4c(var_defs, color_map, components_page),
        "phase4d-corners.js": generate_phase4d(var_defs, corner_map, components_page),
    }
    for name, content in scripts.items():
        (out / name).write_text(content)

    summary = generate_summary(
        var_defs, color_map, corner_map, results, failed,
        components_page, phase1_path, phase2_path, out_dir,
    )
    (out / "SUMMARY.md").write_text(summary)

    print(f"COMPONENTS_PAGE: {components_page}", file=sys.stderr)
    print(f"Variables resolved: {len(var_defs)}", file=sys.stderr)
    print(f"Color map entries: {len(color_map)}", file=sys.stderr)
    print(f"\nGenerated scripts in {out_dir}/", file=sys.stderr)
    for name in scripts:
        print(f"  {name}", file=sys.stderr)
    print("  SUMMARY.md", file=sys.stderr)


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main(argv=None):
    parser = argparse.ArgumentParser(
        description="Generate Phase 4a-4d bind scripts from Phase 1+2 JSON",
        prog="figma_primitives prep-bind",
    )
    parser.add_argument("--phase1", required=True, help="Phase 1 JSON (variable discovery)")
    parser.add_argument("--phase2", required=True, help="Phase 2 JSON (variable validation)")
    parser.add_argument("--config", default=None, help="figma-config.json path (optional)")
    parser.add_argument("--out", default=DEFAULT_OUT, help=f"Output directory (default: {DEFAULT_OUT})")
    args = parser.parse_args(argv)

    run(args.phase1, args.phase2, args.config, args.out)


if __name__ == "__main__":
    main()
